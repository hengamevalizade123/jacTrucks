                مشغول بکار هستند. ظرفیت تولید سالیانه این شرکت ۷۰۰ هزار واحد دستگاه خودرو است. در سال ۲۰۱۱ میزان تولیدات
                این شرکت بالغ بر ۴۶۶۵۰۰ دستگاه خودرو بود. درآمد سالیانه شرکت JAC MOTORS بیش از 4.6 میلیارد دلار تخمین
                زده شده است.
