                شده در عرصه خودورسازی محسوب می‌شود. این شرکت به عنوان سومین شرکت برتر سال، در زمینه صادرات خودرویی کشور
                چین شناخته شده است. شرکت جک موتور در تمام بخش‌ها از جمله کامیون سنگین، سبک و سواری بطور گسترده فعالیت
